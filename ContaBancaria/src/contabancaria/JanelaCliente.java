    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        
        String banco = txtBanco.getText();
        String titular = txtTitular.getText();
        String agencia = txtAgencia.getText();
        String conta = txtConta.getText();
        String dvConta = txtDvConta.getText();
        
        Cliente novo = new Cliente(banco,titular,tipoConta, agencia,conta,dvConta);
        
        dadosBancarios.cadastraCliente(novo);
        
        lblTotal.setText(dadosBancarios.contaClientes() + "Clientes cadastrados.");
        
        System.out.println("DADOS BANCARIOS");
        System.out.print("\n\nBanco: " + novo.getBanco());
        System.out.print("\nTitular: " + novo.getTilular());
        System.out.print("\nTipo de Conta: " + novo.getTipoConta());
        System.out.print("\nAgencia: " + novo.getAgencia());
        System.out.print("\nConta: " + novo.getConta());
        System.out.print("\tDigito: " + novo.getDvConta());
        System.out.println("\n");
        
        //Limpa os campos
        txtBanco.setText("");
        txtTitular.setText("");
        txtAgencia.setText("");
        txtConta.setText("");
        txtDvConta.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

